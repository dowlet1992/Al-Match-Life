def _language_base_code(language_value):
    language_value = str(language_value or "").strip().lower()
    if not language_value:
        return ""

    language_value = language_value.split(",")[0].split(";")[0].strip()
    language_value = language_value.split("-")[0].split("_")[0].strip()
    return language_value


def normalize_language_code(language_value, default=DEFAULT_LANGUAGE):
    language_value = _language_base_code(language_value)
    return language_value if language_value in SUPPORTED_LANGUAGES else default


def normalize_requested_language_code(language_value, default=DEFAULT_LANGUAGE):
    return _language_base_code(language_value) or default


def detect_language(accept_language_header, default=DEFAULT_LANGUAGE):
    for language_item in parse_accept_language_header(accept_language_header):
        language_code = normalize_requested_language_code(language_item["code"], default="")
        if language_code in SUPPORTED_LANGUAGES:
            return language_code
    return default


def parse_accept_language_header(accept_language_header):
    preferences = []
    for index, item in enumerate(str(accept_language_header or "").split(",")):
        parts = [part.strip() for part in item.split(";") if part.strip()]
        if not parts:
            continue

        quality = 1.0
        for part in parts[1:]:
            if not part.startswith("q="):
                continue
            try:
                quality = float(part[2:])
            except ValueError:
                quality = 0.0

        if quality <= 0:
            continue

        preferences.append({
            "code": parts[0].lower(),
            "quality": quality,
            "index": index,
        })

    preferences.sort(key=lambda item: (-item["quality"], item["index"]))
    return preferences


def is_rtl_language(language_value):
    return normalize_requested_language_code(language_value) in RTL_LANGUAGES


def supported_language_options():
    return [
        {
            "code": code,
            "name": name,
            "direction": "rtl" if is_rtl_language(code) else "ltr",
            "translation_status": language_translation_status(code),
            "completion_percent": translation_completion_percent(code),
        }
        for code, name in SUPPORTED_LANGUAGES.items()
    ]


def language_display_name(language_value):
    language_code = normalize_requested_language_code(language_value)
    return LANGUAGE_CATALOG.get(language_code, language_code)


def language_translation_status(language_value):
    language_code = normalize_requested_language_code(language_value)
    if language_code in SUPPORTED_LANGUAGES:
        return "supported"
    if language_code in LANGUAGE_CATALOG:
        return "planned"
    return "unknown"


def translation_completion_percent(language_value):
    language_code = normalize_requested_language_code(language_value)
    required_keys = set(UI_TRANSLATIONS[DEFAULT_LANGUAGE].keys())
    if not required_keys:
        return 0

    current_keys = set(UI_TRANSLATIONS.get(language_code, {}).keys())
    translated_count = len(required_keys & current_keys)
    return int(round((translated_count / len(required_keys)) * 100))


def language_catalog_options():
    return [
        {
            "code": code,
            "name": name,
            "direction": "rtl" if is_rtl_language(code) else "ltr",
            "translation_status": language_translation_status(code),
            "completion_percent": translation_completion_percent(code),
        }
        for code, name in LANGUAGE_CATALOG.items()
    ]


def missing_translation_keys(language_value):
    language_code = normalize_language_code(language_value)
    required_keys = set(UI_TRANSLATIONS[DEFAULT_LANGUAGE].keys())
    current_keys = set(UI_TRANSLATIONS.get(language_code, {}).keys())
    return sorted(required_keys - current_keys)


def translation_coverage_report():
    return {
        code: {
            "name": name,
            "missing_keys": missing_translation_keys(code),
            "complete": not missing_translation_keys(code),
            "completion_percent": translation_completion_percent(code),
        }
        for code, name in SUPPORTED_LANGUAGES.items()
    }


def build_locale_payload(accept_language_header="", requested_language="", user_language=""):
    raw_language = user_language or requested_language
    if not raw_language:
        parsed_languages = parse_accept_language_header(accept_language_header)
        raw_language = parsed_languages[0]["code"] if parsed_languages else accept_language_header

    requested_code = normalize_requested_language_code(raw_language)
    language_code = normalize_language_code(raw_language)
    if not (user_language or requested_language):
        language_code = detect_language(accept_language_header)
    supported = requested_code in SUPPORTED_LANGUAGES

    return {
        "language": language_code,
        "language_name": language_display_name(language_code),
        "requested_language": requested_code,
        "requested_language_name": language_display_name(requested_code),
        "supported": supported,
        "translation_status": language_translation_status(requested_code),
        "translation_completion_percent": translation_completion_percent(requested_code),
        "fallback_language": "" if supported else language_code,
        "direction": "rtl" if is_rtl_language(language_code) else "ltr",
        "requested_direction": "rtl" if is_rtl_language(requested_code) else "ltr",
        "supported_languages": supported_language_options(),
        "language_catalog": language_catalog_options(),
    }


def translate(key, language=None, default_language=DEFAULT_LANGUAGE):
    language = normalize_language_code(language or default_language)
    return UI_TRANSLATIONS.get(language, UI_TRANSLATIONS[default_language]).get(
        key,
        UI_TRANSLATIONS[default_language].get(key, key),
    )


def translation_bundle(language=None):
    language = normalize_language_code(language)
    fallback = UI_TRANSLATIONS[DEFAULT_LANGUAGE]
    bundle = dict(fallback)
    bundle.update(UI_TRANSLATIONS.get(language, {}))
    bundle["language_code"] = language
    bundle["text_direction"] = "rtl" if is_rtl_language(language) else "ltr"
    return bundle
